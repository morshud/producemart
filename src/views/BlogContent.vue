<script>
import MainHeader from "./mainHeader.vue";
import SearchHeader from "./searchHeader.vue";
import MainFooter from "./mainFooter.vue";
import { month } from "../assets/months";
import axios from 'axios'
import Swal from 'sweetalert2';
export default {
  name: "Produce Mart",
  components: {
    mainHeader: MainHeader,
    searchHeader: SearchHeader,
    mainFooter: MainFooter,
  },
  mounted() {
    window.scrollTo(0, 0);
    this.getBlog();
    console.log(this.blog);
  },
  data() {
    return {
      blog: null,
      firstname: '',
      lastname: '',
      email: '',
      group: '',
      company: '',
      role: '',
      country: ''
    };
  },
  methods: {
    async getBlog() {
      const title = this.$route.params.title;
      (this.blog = null), console.log(title);
      const res = await fetch(
        "https://producemart.herokuapp.com/getBlogByTitle",
        {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
          },
          body: JSON.stringify({ title: title }),
        }
      );
      const data = await res.json();
      // console.log(data.data);
      this.blog = data.data[0];
      let d = new Date(this.blog.createdAt);
      this.blog.createdAt =
        d.getDay() + "-" + month[d.getMonth()] + "-" + d.getFullYear();
      //   console.log(this.blog.description);
    },
    newsletterSubscribe(){
      const formData = {
        "email": this.email,
        "firstname": this.firstname,
        "lastname": this.lastname,
        "company": this.company,
        "country": this.country,
        "role": this.role,
        "group": this.group,
      }
      axios.post(`https://producemart.herokuapp.com/subscriberBlog`, formData, {
        headers: {
          "Content-Type": "application/json"
        }
      })
      .then((res) => {
        Swal.fire({
          position: 'top-center',
          icon: 'success',
          title: `You have successfully subscribe to our newsletter`,
          showConfirmButton: true,
          timer: 3500
        })
        this.resetForm()
      })
      .catch(e => {
        console.log(e)
      })
    },
    resetForm(){
      this.email = ''
      this.firstname = ''
      this.lastname = ''
      this.group = ''
      this.company = ''
      this.role = '',
      this.country = ''
    }
  },
};
</script>
